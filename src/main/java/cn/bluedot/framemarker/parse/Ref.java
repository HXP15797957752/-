package cn.bluedot.framemarker.parse;

public class Ref {
    private String potablename;
    private String columnname;
    private String refpotablename;
    private String refname;
    public String getPotablename() {
        return potablename;
    }
    public void setPotablename(String potablename) {
        this.potablename = potablename;
    }
    public String getColumnname() {
        return columnname;
    }
    public void setColumnname(String columnname) {
        this.columnname = columnname;
    }
    public String getRefpotablename() {
        return refpotablename;
    }
    public void setRefpotablename(String refpotablename) {
        this.refpotablename = refpotablename;
    }
    public String getRefname() {
        return refname;
    }
    public void setRefname(String refname) {
        this.refname = refname;
    }
    
}
